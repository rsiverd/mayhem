## Load images:
idata, ihdrs = {}, {}
profile = {}
want_cols = slice(2048-100, 2048+100, 1)
for nn,ipath in tung01_images.items():
    sys.stderr.write("Loading %s ... " % ipath)
    idata[nn], ihdrs[nn] = pf.getdata(ipath, header=True)
    ptemp = np.average(idata[nn][:, want_cols], axis=1)
    ptemp /= np.sum(ptemp)
    profile[nn] = ptemp
    sys.stderr.write("done.\n")

